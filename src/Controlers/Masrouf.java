package Controlers;

public class Masrouf {
    private int idMasrouf;
    private String masroufName;

    public Masrouf(int idMasrouf, String masroufName) {
        this.idMasrouf = idMasrouf;
        this.masroufName = masroufName;
    }

    public int getIdMasrouf() {
        return idMasrouf;
    }

    public String getMasroufName() {
        return masroufName;
    }
}
