package Controlers;

public class Area {
    private int idArea;
    private String nameArea;

    public Area(int idArea, String nameArea) {
        this.idArea = idArea;
        this.nameArea = nameArea;
    }

    public int getIdArea() {
        return idArea;
    }

    public String getNameArea() {
        return nameArea;
    }
}
