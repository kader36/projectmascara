package Controlers;

public class AreaForTable {
    private String areaName;
    private String areaId;

    public AreaForTable(String areaName, String areaId) {
        this.areaName = areaName;
        this.areaId = areaId;
    }

    public String getAreaName() {
        return areaName;
    }

    public String getAreaId() {
        return areaId;
    }
}
